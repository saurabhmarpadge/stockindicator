<h2>How to find a trending stock</h2>

<h3>Increase in Trade Volume</h3><br>
<p>&nbsp&nbsp&nbsp&nbspTrending stocks are always accompanied by high trade volume. Look for stocks with the most trades per day as these are the stocks garnering the most attention. Sudden changes in volume can help identifty posite or negative trends.
</p>
<h3>High Volatility</h3><br>
<p>&nbsp&nbsp&nbsp&nbspTrending stocks also typically have a lot of price variance. Look for stocks with the huge jumps in price on a day-to-day basis. If you can catch the price of a stock on a downswing and sell on an upswing, profit awaits you.
</p>
<h3>Following the News</h3><br>
<p>&nbsp&nbsp&nbsp&nbspBusiness don't operate inside a vaccuum. Pay attention to whats going on in the world. If a tech company releases a new gadget, you bet that investors are watching that stock closely as excitement builds.
</p>
